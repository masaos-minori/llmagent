# ── generate_config_toml_for_role ───────────────────────────────────────────


class TestGenerateConfigTomlForRole:
    def test_sqlite_role_has_db_allowlist(self) -> None:
        result = generate_config_toml_for_role("svc", "sqlite")
        assert "db_allowlist" in result
        assert "max_rows = 100" in result
        assert "http_timeout = 30.0" in result

    def test_shell_role_has_command_allowlist(self) -> None:
        result = generate_config_toml_for_role("svc", "shell")
        assert "command_allowlist" in result
        assert "max_output_kb = 4096" in result
        assert "timeout_sec = 60" in result
        assert "env_allowlist" in result

    def test_git_role_has_repo_and_read_only(self) -> None:
        result = generate_config_toml_for_role("svc", "git")
        assert "allowed_repo_paths" in result
        assert "read_only = true" in result
        assert "max_log_entries = 50" in result
        assert "auth_token" in result
        assert "audit_log_path" in result

    def test_ci_role_has_github_and_repo_allowlist(self) -> None:
        result = generate_config_toml_for_role("svc", "ci")
        assert "github_token" in result
        assert "repo_allowlist" in result
        assert "workflow_allowlist" in result
        assert "max_log_size_kb = 256" in result

    def test_unknown_role_raises_value_error(self) -> None:
        with pytest.raises(ValueError, match="Unknown role"):
            generate_config_toml_for_role("svc", "unknown")


