"""tests/test_installer_templates.py
Unit tests for mcp/installer_templates.py — Template generators for new MCP HTTP servers.
All functions return strings; no file I/O.
"""

from __future__ import annotations

import pytest
from mcp.installer_templates import (
    generate_agent_toml_mcp_snippet,
    generate_confd_template,
    generate_config_toml,
    generate_config_toml_for_role,
    generate_initd_script,
    generate_server_script,
    tool_definition_snippet,
)

# ── generate_server_script ───────────────────────────────────────────────────


class TestGenerateServerScript:
    def test_returns_python_script(self) -> None:
        result = generate_server_script("testserver", "testmod", 8080)
        assert "#!/usr/bin/env python3" in result
        assert "from fastapi import FastAPI" in result

    def test_contains_server_name_and_module(self) -> None:
        result = generate_server_script("myservice", "myservice_mod", 9000)
        assert "myservice-mcp" in result
        assert "mcp.myservice_mod.server" in result
        assert "myservice_mod_sample_tool" in result

    def test_contains_dispatch_and_class(self) -> None:
        # name_to_class("svc") → "Svc" (PascalCase), so class is SvcMCPServer
        result = generate_server_script("svc", "svcmod", 7000)
        assert "_DISPATCH" in result
        assert "SvcMCPServer" in result
        assert "async def dispatch" in result

    def test_contains_health_and_tools_endpoints(self) -> None:
        result = generate_server_script("x", "xmod", 1234)
        assert '@app.get("/health")' in result
        assert '@app.get("/v1/tools")' in result
        assert '@app.post("/v1/call_tool")' in result


